/**
 * Incrementing 
 */
var number = 0;
console.log(number);

number = number + 1
console.log(number);

number += 1;
console.log(number);

number++;
console.log(number);


/**
 * Decrementing
 */
var value = 100;
console.log(value);

value = value - 1;
console.log(value);

value -= 1;
console.log(value);

value --;
console.log(value);

